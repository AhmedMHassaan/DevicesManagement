    private void getFormData() {
        ArrayList<DeviceType> types = getAllTypes();
        if (types != null) {
            allTypes.clear();
            allTypes.addAll(types);

            fillTypesTable(types);
        }

//        emptyFormFields(this);
        txtAddType.setText("");
        lbTypeId.setText("");
        txtTypeNameForUpdate.setText("");
        tableTypes.setSelectionModel(new DefaultListSelectionModel());

        changeUpdateTypeControllesVisibility(false);
    }

    private ArrayList<data.pojo.DeviceType> getAllTypes() {
//        return TypesRepository.getInstance().getAllTypes();
        return TypesRepository.getInstance().getAllLocalTypes();
    }

    private void fillTypesTable(ArrayList<data.pojo.DeviceType> types) {
        tableTypes.clearTable();
        DefaultTableModel model = tableTypes.getMutableModel();

        types.forEach((type) -> {
            model.addRow(new Object[]{
                type.getTypeId(),
                type.getTypeName(),
                type.getDevicesCountInType()
            });
        });
        tableTypes.setModel(model);
    }

    private data.pojo.DeviceType getTypeFromSelectedRow(int selectedRowIndex) {
        return allTypes.get(selectedRowIndex);
    }

    private void showItemDetails(data.pojo.DeviceType type) {
        lbTypeId.setText(type.getTypeId() + "");
        txtTypeNameForUpdate.setText(type.getTypeName());
        lblDevicesCount.setText(type.getDevicesCountInType() + "");
    }

    private void changeUpdateTypeControllesVisibility(boolean isVisible) {
        btnSaveChanges.setVisible(isVisible);
        lblDeleteBrand.setVisible(isVisible);
    }

    private boolean addTypeToDB(String type) {
//        return TypesRepository.getInstance().addNewType(type);
        return TypesRepository.getInstance().addNewLocalType(type);
    }

    private boolean deleteType(DeviceType type) {
//        return TypesRepository.getInstance().deleteType(type.getTypeId());
        return TypesRepository.getInstance().deleteLocalType(type.getTypeId());
    }

    private boolean updateType(DeviceType type) {
//        return TypesRepository.getInstance().updateType(type.getTypeId(), type.getTypeName());
        return TypesRepository.getInstance().updateLocalType(type.getTypeId(), type.getTypeName());
    }

    private void emptyFormFields(Container container) {
        for (Component comp : container.getComponents()) {
            if (comp instanceof JTextField) {
                ((JTextField) comp).setText("");
            } else if (comp instanceof JLabel) {

                ((JLabel) comp).setText("");
            } else if (comp instanceof Container) {
                emptyFormFields((Container) comp);
            }
        }
    }

