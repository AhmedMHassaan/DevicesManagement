    private void tableTypesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableTypesMouseClicked
        if (evt.getButton() == MouseEvent.BUTTON1) {
            if (evt.getClickCount() == 1) {
                int selectedRowIndex = tableTypes.getSelectedRow();
                data.pojo.DeviceType type = getTypeFromSelectedRow(selectedRowIndex);
                showItemDetails(type);
                changeUpdateTypeControllesVisibility(true);
            }
        }
    }//GEN-LAST:event_tableTypesMouseClicked

    private void btnAddTypeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddTypeActionPerformed
        String type = txtAddType.getText().trim();
        if (type.isEmpty()) {
            MessageBox.showErrorMessage("يرجي كتابة اسم النوع");
            return;
        }

        boolean isAdded = addTypeToDB(type);
        if (isAdded) {
            getFormData();
//            insertTypeToTable(type);
        }
    }//GEN-LAST:event_btnAddTypeActionPerformed

    private void lblDeleteBrandMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblDeleteBrandMouseClicked

        int delOption = JOptionPane.showConfirmDialog(this, "سيتم حذف النوع  نهائيا\nهل تريد المتابعة", "حذف " + txtTypeNameForUpdate.getText(), JOptionPane.YES_NO_OPTION);
        if (delOption == JOptionPane.YES_OPTION) {
            int selectedIndex = tableTypes.getSelectedRow();
            if (selectedIndex == -1) {
                MessageBox.showErrorMessage("يرجي اختيار نوع أولا للحذف");
                return;
            }

            data.pojo.DeviceType type = allTypes.get(selectedIndex);
            boolean isDeleted = deleteType(type);
            if (isDeleted) {
                lbTypeId.setText("");
                txtTypeNameForUpdate.setText("");
                tableTypes.setSelectionModel(new DefaultListSelectionModel());
                getFormData();
            }
        }

    }//GEN-LAST:event_lblDeleteBrandMouseClicked

    private void btnSaveChangesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveChangesActionPerformed

        String typeName = txtTypeNameForUpdate.getText().trim();
        int selectedIndex = tableTypes.getSelectedRow();

        if (selectedIndex == -1) {
            MessageBox.showErrorMessage("يرجي اختيار نوع أولا لإكمال التعديل");
            return;
        }

        if (typeName.isEmpty()) {
            MessageBox.showErrorMessage("يرجي كتابة اسم النوع");
            return;
        }

        data.pojo.DeviceType type = allTypes.get(selectedIndex);
        type.setTypeName(typeName);
        boolean isUpdated = updateType(type);
        if (isUpdated) {
            getFormData();
        }
    }//GEN-LAST:event_btnSaveChangesActionPerformed

