    private void getFormData() {
        ArrayList<Department> deps = getAllDepartments();
        if (deps != null) {
            allDepartments.clear();
            allDepartments.addAll(deps);

            fillDepartmentsTable(allDepartments);
        }

        lblDepId.setText("");
        txtAddDepartment.setText("");
        txtDepNameForUpdate.setText("");
        tableDeps.setSelectionModel(new DefaultListSelectionModel());

    }

