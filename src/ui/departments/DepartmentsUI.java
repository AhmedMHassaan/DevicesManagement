    private void tableDepsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDepsMouseClicked
        if (evt.getButton() == MouseEvent.BUTTON1) {
            if (evt.getClickCount() == 1) {
                int selectedRowIndex = tableDeps.getSelectedRow();
                Department dep = getDepartmentFromSelectedRow(selectedRowIndex);
                showItemDetails(dep);
                changeUpdateDepControllesVisibility();
            } else if (evt.getClickCount() == 2) {
                int selectedRowIndex = tableDeps.getSelectedRow();
                Department dep = getDepartmentFromSelectedRow(selectedRowIndex);
                startNewFrameWithOldFrame(DevicesUI.getInstance(dep.getDepartmentName(),-1,-1));
            }
        }
    }//GEN-LAST:event_tableDepsMouseClicked

    private void btnAddDepartmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddDepartmentActionPerformed
        String department = txtAddDepartment.getText().trim();
        if (department.isEmpty()) {
            MessageBox.showErrorMessage("يرجي كتابة اسم الفرع");
            return;
        }

        boolean isAdded = addDepartmentToDB(department);
        if (isAdded) {
            getFormData();
        }
    }//GEN-LAST:event_btnAddDepartmentActionPerformed

    private void lblDeleteDepMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblDeleteDepMouseClicked

        int selectedIndex = tableDeps.getSelectedRow();
        Department department = allDepartments.get(selectedIndex);
        
        int delOption = JOptionPane.showConfirmDialog(this, "سيتم حذف الفرع "+department.getDepartmentName()+"  نهائيا\nهل تريد المتابعة", "حذف الفرع", JOptionPane.YES_NO_OPTION);
        if (delOption == JOptionPane.YES_OPTION) {

            if (selectedIndex == -1) {
                MessageBox.showErrorMessage("يرجي اختيار فرع أولا للحذف");
                return;
            }

            boolean isDeleted = deleteDepartment(department);
            if (isDeleted) {
                lblDepId.setText("");
                txtDepNameForUpdate.setText("");
                tableDeps.setSelectionModel(new DefaultListSelectionModel());
                getFormData();
            }
        }

    }//GEN-LAST:event_lblDeleteDepMouseClicked

    private void btnSaveChangesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveChangesActionPerformed

        String depName = txtDepNameForUpdate.getText().trim();
        int selectedIndex = tableDeps.getSelectedRow();

        if (selectedIndex == -1) {
            MessageBox.showErrorMessage("يرجي اختيار فرع أولا لإكمال التعديل");
            return;
        }

        if (depName.isEmpty()) {
            MessageBox.showErrorMessage("يرجي كتابة اسم الفرع");
            return;
        }

        Department dep = allDepartments.get(selectedIndex);
        dep.setDepartmentName(depName);
        boolean isUpdated = updateDepartmet(dep);
        if (isUpdated) {

            getFormData();
        }
    }//GEN-LAST:event_btnSaveChangesActionPerformed

